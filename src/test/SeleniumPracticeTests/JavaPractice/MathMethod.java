package SeleniumPracticeTests.JavaPractice;

public class MathMethod {
    int num1;
    int num2;

     public int addMethod(int num1, int num2){
         this.num1 = num1;
         this.num2 = num2;

         return num1 + num2;
     }

     public int subMethod(int num1, int num2){
         this.num1 = num1;
         this.num2 = num2;

         return num1 - num2;
     }

     public int multiMethod(int num1, int num2){
         this.num1 = num1;
         this.num2 = num2;

         return num1 * num2;
     }

     public int divMethod(int num1, int num2){
         this.num1 = num1;
         this.num2 = num2;

         return num1 / num2;
     }
}
